
package model;

import java.time.Year;

public class model_unit {
    private String no_pol;
    private String merek;
    private String model;
    private String tipe;
    private String tahun_produksi;
    private String warna; 

    public String getNo_pol() {
        return no_pol;
    }

    public void setNo_pol(String no_pol) {
        this.no_pol = no_pol;
    }

    public String getMerek() {
        return merek;
    }

    public void setMerek(String merek) {
        this.merek = merek;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getTipe() {
        return tipe;
    }

    public void setTipe(String tipe) {
        this.tipe = tipe;
    }

    public String getTahun_produksi() {
        return tahun_produksi;
    }

    public void setTahun_produksi(String tahun_produksi) {
        this.tahun_produksi = tahun_produksi;
    }

    public String getWarna() {
        return warna;
    }

    public void setWarna(String warna) {
        this.warna = warna;
    }

    
}
